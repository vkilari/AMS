public class Banners {

    private String banner_Id;
    private String bannerName;
    private String bannerType;
    private String bannerProvider;
    private String promotionStart;
    private String promotionEnd;
    private String bannerImage;
    private String bannerDetails;

    public String getBanner_Id() {
        return banner_Id;
    }

    public void setBanner_Id(String banner_Id) {
        this.banner_Id = banner_Id;
    }

    public String getBannerName() {
        return bannerName;
    }

    public void setBannerName(String bannerName) {
        this.bannerName = bannerName;
    }

    public String getBannerType() {
        return bannerType;
    }

    public void setBannerType(String bannerType) {
        this.bannerType = bannerType;
    }

    public String getBannerProvider() {
        return bannerProvider;
    }

    public void setBannerProvider(String bannerProvider) {
        this.bannerProvider = bannerProvider;
    }

    public String getPromotionStart() {
        return promotionStart;
    }

    public void setPromotionStart(String promotionStart) {
        this.promotionStart = promotionStart;
    }

    public String getPromotionEnd() {
        return promotionEnd;
    }

    public void setPromotionEnd(String promotionEnd) {
        this.promotionEnd = promotionEnd;
    }

    public String getBannerImage() {
        return bannerImage;
    }

    public void setBannerImage(String bannerImage) {
        this.bannerImage = bannerImage;
    }

    public String getBannerDetails() {
        return bannerDetails;
    }

    public void setBannerDetails(String bannerDetails) {
        this.bannerDetails = bannerDetails;
    }
}
