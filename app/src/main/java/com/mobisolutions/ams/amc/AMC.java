public class AMC {

    private String amcID;
    private String amcName;
    private String amcUpdatedDate;

    public String getAmcID() {
        return amcID;
    }

    public void setAmcID(String amcID) {
        this.amcID = amcID;
    }

    public String getAmcName() {
        return amcName;
    }

    public void setAmcName(String amcName) {
        this.amcName = amcName;
    }

    public String getAmcUpdatedDate() {
        return amcUpdatedDate;
    }

    public void setAmcUpdatedDate(String amcUpdatedDate) {
        this.amcUpdatedDate = amcUpdatedDate;
    }
}
